			<h1 style="font-weight: 300">History</h1>
			<p style="padding: 20px 20px;">HETAC (Home Economics Teachers Association of California) was formed in July 1981 after a survey of California home economics teachers indicated a need for an organization that would focus on issues concerning secondary and adult CFS and HERO programs in California.  In 2017, the name was changed to Family and Consumer Sciences Teachers Association of California (FCSTAC) to reflect the changing language used at the state department of education and the names more commonly used across the state for Family and Consumer Science classes.  FCSTAC elects officers to serve a 2-year term, has quarterly executive board meetings, assists with many coordinated professional development opportunities for California Family and Consumer Sciences Teachers. FCSTAC is proud to sponsor Teacher of the Year, Dream Dare and Do, FCCLA FCSTAC Scholarship and FCCLA Honor Chapters. Currently, FCSTAC is engaged in legislative action to ensure permanent, appropriate funding and CDE support for Family and Consumer Sciences programs across the state.
