			<h1 style="font-weight: 300">About</h1>
			<p style="padding: 20px 20px;">FCTAC is an influential and effective organization that advocates for home economics careers and technology teachers on issues impacting their programs and curriculum.</p>
			
			<h1 style="font-weight: 300">History</h1>
			<p style="padding: 20px 20px;">HETAC was formed in July 1981 after a survey of California home economics teachers indicated a need for an organization that would focus on issues concerning secondary and adult CFS and HERO programs in California.
HETAC elects officers to serve a 2-year term, has quarterly executive board meetings, assists with many coordinated professional development opportunities for California Home Economics Careers and Technology Teachers. HETAC is proud to sponsor Teacher of the Year, Dream Dare and Do, FHA-HERO HETAC Scholarship and FHA-HERO Honor Chapters.
Currently, local secondary Home Economics Careers and Technology programs in California have no guaranteed level of sustained, supplemental funding (beyond minimal basic state apportionment) at either the state or federal level which explains why HETAC remains vigilant of state legislation.
			</p>
			
			<h1 style="font-weight: 300">Mission and Purpose</h1>
			<p style="padding: 20px 20px;">
To increase networking and communication among Home Economics Careers and Technology teachers
To keep teachers informed of the latest educational issues
To encourage high standards of teaching
To expand and improve public knowledge of Home Economics Careers and Technology
HETAC is a professional organization focusing of the unique needs of Home Economics Careers and Technology Teachers
To actively promote the interests of home economics careers and technology teachers
To support FHA-HERO as an integral part of home economics careers and technology programs
To recognize and honor individuals for outstanding contributions to home economics careers and technology</p>
